package nyg;

public class Sweet {

    String name;
    int w;
    int p;

    public Sweet(String name, int w, int p) {
        this.name = name;
        this.w = w;
        this.p = p;
    }

    public String getName() {
        return name;
    }

    public int getW() {
        return w;
    }

    public int getP() {
        return p;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setW(int w) {
        this.w = w;
    }

    public void setP(int p) {
        this.p = p;
    }
}
